# pole koła
WartoscPi=3.14
PromienOkregu=5
PoleKola=WartoscPi*(PromienOkregu**2)
print ('Pole koła:',PoleKola)

# obwód okręgu
ObwodOkregu=2*WartoscPi*PromienOkregu
print ('Obwód okręgu:',ObwodOkregu)

