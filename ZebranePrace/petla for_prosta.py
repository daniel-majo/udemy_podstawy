''' range tworzy listę wartosci całkowitych od 0 do podanej w ().
jesli podamy 1 to będą to dwa argumenty, 0 i 1'''

# drukuje od 1 do 9 jedynkami
print ("Pętla 1:")
for i in range(10):
    print (i)

# drukuje od 3 do 4
print ("Pętla 2:")
for i in range(3,5):
	print (i)

# drukuje od 10 do 90 dziesiątkami
print ("Pętla 3:")
for i in range(10, 100, 10):
	print (i)
