print("Program wyznaczający wartość bezwzględną")
liczba = int(input("Podaj liczbę "))

if (liczba > 0):
    print ("Wartość bezwzględna liczby podanej przez Ciebie ", liczba, "wynosi ", liczba)
elif (liczba==0):
    print ("Wartość bezwzględna 0 to 0")
else:
    print ("Wartość bezwzględna liczby podanej przez Ciebie ", liczba, "wynosi ", -liczba)
    
